            <h3>UITLEG</h3>
            </span>
            </div>
            
    	<div class="projecttext">
        <p>We zijn weer begonnen met het maken van 250 ideeën maar hierbij gericht op technologie. Daarbij hebben we apart van elkaar ideeën verzonnen. Ik ging zoeken op internet naar ideeën. Ik ging kijken naar verschillende technieken en gadgets die nu op de markt zijn. Deze heb ik daarna op post its geschreven en op het bord geplakt. We hadden het in een aantal categorieën gedeeld: licht, muziek, drones/VR etc., publiek en overige. </p>
        <p>
        Daarna zijn we de ideeën gaan clusteren. Deze ideeën hebben we daarna in een top 3 gezet welke we het beste vonden. Deze ideeën hebben we daarna gedocumenteerd.
        </p>

        <div id="gallery-3" class="gallery galleryid-2178 gallery-columns-3 gallery-size-thumbnail"><div class="clearboth"></div><dl class=" gallery-fullwidth">
            <dt class="gallery-icon landscape">

        <img src="../images/scoweek2.png"class="attachment-thumbnail" height="548" width="958">
            </dt></dl>
        </div>

        <p>We kregen van de twee docenten feedback van de concepten van vorige week. We kregen erg positieve feedback. Ze vonden dat we het goed abstract hadden gehouden. Verder wilden ze nog wel erbij hebben wat voor middelen we gaan gebruiken. We hebben toen meteen onze ideeën van deze week laten zien. ze vonden het leuke ideeën, maar het waren nog geen concepten. Ze zeiden dat we bijvoorbeeld deze ideeën konden combineren met onze vorige concepten. Verder vonden ze dat we goed bezig waren. </p>

        <p>We zijn daarna de concepten gaan maken. We hadden een aantal ideeën gecombineerd met de concepten van vorige week zodat het een nieuw concept werd. Zo heb ik bedacht om de verschillende technologieën project mapping, AR stage en glow in the dark bij elkaar te voegen. Dit in combinatie met het concept vergrijzing. Daarna hebben we het concept rust gepakt en hierbij technieken toegevoegd zodat het een nieuw concept werd. Daarbij heb ik bedacht om de omnimax theater idee te gebruiken voor dit concept. Zo kan je filmpjes tonen die een rustige sfeer geven. Ook leek het me een goed idee om drones te gebruiken voor het bedienen van eten. Voor het concept heb ik een moodboard gemaakt.</p>

        <div id="gallery-3" class="gallery galleryid-2178 gallery-columns-3 gallery-size-thumbnail"><div class="clearboth"></div><dl class=" gallery-fullwidth">
            <dt class="gallery-icon landscape">

        <img src="../images/conceptvisual.png"class="attachment-thumbnail" height="548" width="958">
            </dt></dl>
        </div>

<p>Van het vak DEV kregen we nog een workshop. Daar kregen we voorbeelden van verschillende campagnes die nieuwe technologieën gebruiken. Daarna moest elk groepje een technologie uitkiezen waarbij er een bestaand campagne van gezocht moest worden. Wij hadden de technologie projection mapping gekozen. Ik vond dit een interessant technologie, omdat dit in één van mijn concepten was verwerkt. We hebben een aantal campagnes gevonden die project mapping gebruikt. Een daarvan was Adidas. Deze had op een spectaculaire wijze op een gebouw project mapping gedaan. Daarna heeft elke groepje een campagne gepresenteerd met hun gekozen technologie. het was erg inspirerend om verschillende technologieën in campagnes verwerkt te zien. Sommige waren erg creatief en sommige technologieën had ik nog nooit van gehoord.
</p>

<p>Daarna hebben we nog twee concepten verzonnen. Dit zullen we volgende week weer laten zien voor feedback.</p><br>

