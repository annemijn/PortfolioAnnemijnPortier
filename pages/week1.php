<p>Daarna zijn we met het projectgroepje de 250 ideeën gaan samenvoegen. De ideeën die iets met elkaar te maken hadden zijn we bij elkaar gaan zetten. Daarna heeft iedereen twee rode, blauwe en gele stickers gekregen. De rode betekende origineel en toepasbaar, de blauwe betekende niet origineel en toepasbaar en de gele betekende origineel maar niet toepasbaar. Deze stickers zijn we toen gaan plakken op de ideeën die we daarbij vonden horen. Daarna zijn we concepten gaan verzinnen met de ideeën die stickers hadden gekregen. </p>

<p>We kwamen op het concept “bring classic to the mainstream”. Door het klassieke orkest op plaatsen te laten spelen waar veel jongeren komen/zijn willen we op deze manier de interesse bij de jongeren opwekken. Hierbij denken wij aan locaties zoals bijvoorbeeld grote winkelcentrums, stations of gewoon in het centrum van de stad. Zo brengen we klassieke muziek naar de jongeren toe.</p>

<p>Een andere concept was “verjongen”. Door huidige festival- en jongerentrends naar de klassieke muziek te brengen krijg je een moderne frisse, sfeer bij het concert. Door bijvoorbeeld gebruik te maken van leuke locaties zoals stranden, festivalweiden etc. en het concert op een andere manier aan te kleden trek je nieuwe bezoekers. Er kan gebruik worden gemaakt van kleurpoeder, schuimkanonnen, drones etc. Ook door bv. populaire DJ’s samen te laten werken met orkesten creëer je een mix van klassieke muziek en moderne muziek. Hierdoor wordt klassieke muziek weer in een nieuw jasje gestoken.</p>

<p>En het derde concept was een viral video. In de openbare ruimte worden veel handgebaren e.d. gebruikt, deze gebaren lijken op die van een dirigent. Door geluiden toe te voegen bij deze gebaren schrikken de voorbijgangers. Ze proberen het een paar keer uit en dan wordt het orkest onthuld. De voorbijganger is verbaasd. Dit wordt allemaal gefilmd en gemonteerd tot een viral video.
Deze concepten zullen we aan onze tutor voorleggen.<br>
</p>

