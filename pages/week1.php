            <h3>INLEIDING</h3>
            </span>
            </div>
            
    	<div class="projecttext">
<p>Voor mij is dit de tweede keer dat ik aan semester 4 ga deelnemen. Ik heb het helaas de vorige keer niet gehaald. Ik wil het dit halfjaar anders ga doen. Ik wil een meer productieve en ondernemende houding nemen bij het maken van de concepten. 

Ik merkte al vrijwel meteen dat het semester een stuk veranderd was. Dit komt waarschijnlijk ook omdat ik een overstap van Tilburg naar Eindhoven heb gedaan. We mochten zelf groepjes maken waarmee we met het project aan de slag zullen gaan. Omdat ik niemand kende uit Eindhoven ben ik maar bij een random groepje aangesloten. Het was best chaotisch omdat de twee klassen moesten wisselen van lokaal. Toen ze gingen uitleggen hoe deze semester zal gaan verlopen merkte ik ook al meteen een verschil met het semester van vorig jaar. Er zullen namelijk vier weken van verzinnen van concepten worden uitgevoerd. Elke week zal een andere thema zijn. Hierdoor krijg je uiteindelijk ongeveer 25 concepten krijgen waar je twee kan uitkiezen. Dit vond ik zo fijn. Dit was juist het probleem die ik vorig jaar had. Te weinig brainstormen en te snel een concept kiezen. Ik was hier heel blij mee. 
</p>
        </div>
                 <div class="titeluitleg">
            <span>
            <h3>UITLEG</h3>
            </span>
            </div>
            
    	<div class="projecttext">
        <p>Als eerste kregen we een gastles van de opdrachtgever van de andere klas. Hij legde heel duidelijk uit wat precies een concept is en een idee. Dit was veel duidelijker uitgelegd als de vorige keer. Daarbij wist ik pas na een aantal workshops van sco wat nou het verschil was tussen een concept en een idee. 
</p>

<p><span>Workshop SCO</span><br>
Na de gastles kregen we de eerste worskhop van SCO. In deze workshop gingen we met ons groepje 250 ideeën bedenken. Dit deden we met een aantal brainstormtechnieken. Deze had ik gekozen om uit te proberen:
</p>
<p>1.   Benen op tafel<br>
2.  Associaties plaatjes<br>
3.  Negatief brainstormen<br>
4.  ketenassociatie
</p>

<p>Als eerste zijn we de brainstormtechniek “benen op tafel” gaan doen. Daarbij was het de bedoeling dat je in een fijne houding met elkaar kletst. Daaruit zullen dan zelf ideeën uit voort doen. Dit ging moeizaam. We kregen niet veel inspiratie van deze techniek. Iedereen was nog niet echt opgewarmd voor het verzinnen van ideeën. We zijn dan ook maar verder gegaan naar het tweede techniek. Ik raadde aan om de “associaties plaatjes” techniek te gaan gebruiken. Door random plaatjes op te zoeken kan je hiervan inspiratie krijgen voor een idee. Ik merkte bij iedereen dat we wat losser werden en op meer ideeën kwamen. We dachten meer outside the box en hielden ons niet teveel aan de regels. Uiteindelijk hadden we het een beetje gehad met de random plaatjes en gingen we naar de volgende brainstormtechniek. Hierbij gingen we negatief brainstormen. Dit was lastiger als we dachten. Als eerste dachten we namelijk dat je negatieve ideeën moest verzinnen voor de opdracht. Dit was niet logisch want we hadden aan deze ideeën dus niks. Toen dacht ik er opeens aan om juist eerst de negatieve idee te verzinnen en dit om te zetten naar een positieve idee. Met dit kreeg ik best veel ideeën die ik op had geschreven. Ik merkte dat dit niet bij iedereen in het groepje de techniek zo goed ging. Daarom zijn we een volgende brainstormtechniek gaan uitproberen, de ketenassociatie. Hierbij moest iemand een startwoord verzinnen en moest iemand anders van dat woord een associatie verzinnen. Dit ging zo door totdat we op een idee uitkwam. Met deze brainstormtechniek kwamen we op een hoop ideeën. Uiteindelijk hebben we er ongeveer 250 ideeën verzonnen.</p>

