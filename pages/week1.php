            <p><span>Workshop 2 SCO</span><br>
Dinsdag kregen we de tweede workshop van SCO. We moesten een crossmediale campagne uitzoeken die ons aansprak.</p>

<p>
Als eerste kwam ik uit op een campagne van de vliegtuigmaatschappij <a href="http://onemileatatime.boardingarea.com/2016/09/19/casey-neistat-emirates/">Emirates</a>. Hierbij ging het om de strijd tussen de Amerikaanse en de Midden oosten Airlines. In deze campagne liet Emirates zien dat de Amerikanen ook met deze airline reizen. Hiervoor hebben ze Jennifer Aniston gekozen. Daarbij wilden ze dus aan de amerikanen laten zien dat Amerikaanse beroemdheden met Emirates graag reizen. Ze hebben gebruik gemaakt van een advertentie. Hierin lieten ze Jennifer Aniston in een advertentie zien hoe leuk ze het in een Emirates vliegtuig heeft.
Verder hebben ze ook gebruik gemaakt van Youtube. Daarbij hebben ze de bekende Amerikaanse youtuber Casey een gratis ticket gegeven in ruil voor dat hij ging vloggen. Dit filmpje is wereldberoemd geworden.
</p>

<p>Wat ik hierna merkte is dat het geen crossmediale campagne is. Er zijn namelijk geen drie media hiervoor gebruikt. Daarom ben ik verder gaan zoeken naar een wel echte crossmediale campagne.</p>

<p><a href="http://shortyawards.com/7th/tidy-cats-kitten-week">Kittenweek</a> is een campagne van Tidy. Met kittenweek maken ze een parody op de bekende “sharkweek” in Amerika. Door middel van video’s laten ze “enge” kittens zien op hun youtubekanaal. Daarbij wordt er een verhaal verteld van een persoon die een enge ervaring heeft met een kitten. Ook op de twitter en facebook accounts van Tidy posten ze gifs, memes, kittenfacts en plaatjes van kittens. Dit is vooral gericht op de engheid van de kitten.
Kittenweek is een concept omdat er verschillende ideeën uit voortkomen. Zoals de dramatische kitten video’s die op youtube worden geplaatst en plaatjes op twitter of facebook die tonen hoe eng deze kittens zijn. De media’s die worden gebruikt zijn Facebook, Twitter en Youtube. Dit maakt het een cros mediale campagne.
</p>
